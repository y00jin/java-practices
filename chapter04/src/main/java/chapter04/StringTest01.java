package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");
		// "hello"
		System.out.println("\"hello\"");
		
		// 제어문자
		// \t = tab
		// \n = new line(enter)
		// \r = Carriage Return
		System.out.print("Hello\tWorld\r\n");
		System.out.print("Hello\tWorld\n");
		
		//  '
		char c = '\'';
		System.out.println(c);
	}
	
}
